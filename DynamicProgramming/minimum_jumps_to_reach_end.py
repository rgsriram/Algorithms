def min_jump(arr):
    n = len(arr)
    ladder = stairs = arr[0]
    jumps = 1

    for i in range(1, n):

        if i == (n-1):
            return jumps

        ladder = max(ladder, arr[i] + i)

        stairs -= 1
        if stairs == 0:
            jumps += 1

            if i >= ladder:
                return -1

            stairs = ladder - i

    return -1


